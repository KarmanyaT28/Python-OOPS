# The class that inherits another class is called a child class and 
# the class that gets inherited is called a base class or parent class.

# The syntax for creating a parent class is shown below −

# class ParentClassName:
#    {class body}


# Following is the syntax of child class −

# class SubClassName (ParentClass1[, ParentClass2, ...]):
#    {sub class body}


# Types of Inheritance
# In Python, inheritance can be divided in five different categories −

# Single Inheritance
# Multiple Inheritance
# Multilevel Inheritance
# Hierarchical Inheritance
# Hybrid Inheritance




