# Accessing tuple items with indexing


tuple1 = ("Rohan", "Physics", 21, 69.75)
tuple2 = (1,2,3,4,5)


print(tuple1)
print(tuple2)

print("Item at 0th index in tuple1: ", tuple1[0])
print("Item at 2nd index in tuple2: ", tuple2[2])


# Accessing tuple items with negative indexing


tupj = ("a", "b", "c")
tupk = (25.50, True, -55, 1+2j)


print ("Item at last index in tupj: ", tupj[-1])
print ("Item at index 1 in tupk: ", tupk[-3])