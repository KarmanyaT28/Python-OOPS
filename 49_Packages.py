# Python - Packages
# ✅ Definition:

# A package is a way of organizing related Python modules (files) into a directory structure.

# It allows large applications to be divided into smaller, reusable components.

# Every package must contain an __init__.py file (can be empty) to be recognized as a package.

# 📝 Notes:

# Module = A single Python file (.py).

# Package = A folder containing multiple modules.

# Promotes code reusability and maintainability.



from math_pkg import add, subtract

print(add.add(10, 5))       # 15
print(subtract.subtract(10, 5))  # 5


